static UINT32 CFW_Cfg_atoi( UINT32 *pVal, UINT8 *s)
{
    UINT32 err = 0;
    UINT8 c;
    UINT32 val = 0;
    CSW_PROFILE_FUNCTION_ENTER(CFW_Cfg_atoi);
    while ( (c = *s++) != 0x00)
    {
        if (c >= '0' && c <= '9')
        {
            c -= '0';
        }
        else
        {
            err = 0;
            break;
        }

        err = 1;
        val = (val * 10 + c);
    }
    *pVal = val;
    CSW_PROFILE_FUNCTION_EXIT(CFW_Cfg_atoi);
    return err;
}


