INT32 CFW_Cfg_strcmp(const UINT8 *s1, const UINT8 *s2)
{
    UINT8     c1 = 0;
    UINT8     c2 = 0;
    INT32    ret = 0;
    CSW_PROFILE_FUNCTION_ENTER(CFW_Cfg_strcmp);
    if(!s1 || !s2)
    {
        CSW_PROFILE_FUNCTION_EXIT(CFW_Cfg_strcmp);
        return 1;
    }

    do
    {
        while ( (c1 = *s1++) == ' ');

        while ( (c2 = *s2++) == ' ');

        ret = c1 - c2;

    }
    while ( ret == 0 && c1 != '\0' && c2 != '\0' );
    CSW_PROFILE_FUNCTION_EXIT(CFW_Cfg_strcmp);
    return ret;
}



