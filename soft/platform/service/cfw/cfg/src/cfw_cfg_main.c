static void GetString(UINT8 *pOUtStr, UINT8 **pStr)
{
    UINT8 *ptmp = 0;
    UINT8 *pBegin = 0;
    UINT8 len = 0;
    CSW_PROFILE_FUNCTION_ENTER(GetString);
    ptmp = (*pStr);
    pBegin = ptmp;
    while((*ptmp != ',') && (*ptmp != 0))
    {
        ptmp++;
        len++;
        (*pStr)++;
    }
    SUL_MemCopy8(pOUtStr, pBegin, len);
    (*pStr)++;
    CSW_PROFILE_FUNCTION_EXIT(GetString);
}


